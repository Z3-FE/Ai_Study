// SQL code generated by agent in the second screen "执行详情 -> SQL" tab
export const generatedSQLContent = `SELECT
  DATE(o.order_purchase_timestamp) AS date,
  SUM(p.payment_value) AS sales_amount,
  COUNT(DISTINCT o.order_id) AS order_count,
  COUNT(DISTINCT c.customer_id) AS active_customers,
  SUM(p.payment_value) / COUNT(DISTINCT o.order_id) AS avg_order_value,
  ROUND(
    SUM(oi.price * oi.freight_value * -1) 
    / NULLIF(SUM(p.payment_value), 0) * 100, 2
  ) AS refund_rate
FROM orders o
JOIN order_items oi ON o.order_id = oi.order_id
JOIN payments p ON o.order_id = p.order_id
JOIN customers c ON o.customer_id = c.customer_id
WHERE o.order_status = 'delivered'
  AND p.payment_type IN ('credit_card','debit_card','boleto')
  AND o.order_purchase_timestamp
    BETWEEN '2024-05-01' AND '2024-05-19'
GROUP BY DATE(o.order_purchase_timestamp)
ORDER BY date ASC
LIMIT 1000;`;
